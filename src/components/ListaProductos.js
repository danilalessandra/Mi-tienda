import React, { Component } from 'react';
import Producto from '../components/Producto';
import Carrito from '../components/Carrito'  

class ListaProductos extends Component {
    state = {
        productos: [
            { 
                id: 1, 
                nombre: "LIP CASE", 
                precio: 38, 
                imagen: "/images/lip_case.png" 
            },
            { 
                id: 2, 
                nombre: "THE POCKET BLUSHES", 
                precio: 130, 
                imagen: "/images/pocket_blushes.png"
            },
            { 
                id: 3, 
                nombre: "THE PEPTIDE LIPS TINS", 
                precio: 65, 
                imagen: "/images/peptide_lips_tins.png"
            }
        ],
        carrito: []
    };

    agregarAlCarrito = (producto) => {
        this.setState(prevState => ({
            carrito: [...prevState.carrito, producto]
        }));
    };

    render() {
        return (
            <div>
                <h1>Lista de Productos</h1>
                {this.state.productos.map((producto) => (
                    <Producto
                        key={producto.id}
                        producto={producto}
                        agregarAlCarrito={this.agregarAlCarrito}
                    />
                ))}
                <Carrito carrito={this.state.carrito} />  {/* Mostrar el carrito */}
            </div>
        );
    }
}

export default ListaProductos;


       
 


 
 
 
 
 

 
 
 
 
 
 
 
 
 
 
 
 
 
 
 































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































